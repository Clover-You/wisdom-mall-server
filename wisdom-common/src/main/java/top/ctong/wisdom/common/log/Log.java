@Retention(RetentionPolicy.RUNTIME)
@Documented
public @interface Log {

    /**
     * 是否记录请求体，如果存在大量数据，可能需要关闭它
     */
    boolean recordRequestBody() default true;

    /**
     * 是否记录请求结果，如果 data=null 那么不记录
     */
    boolean recordResult() default true;

    String name();

}
